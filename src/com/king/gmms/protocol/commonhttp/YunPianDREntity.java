package com.king.gmms.protocol.commonhttp;

public class YunPianDREntity {
	private long sid;
	private long uid;
	private String user_receive_time;
	private String error_msg;
	private String mobile;
	private String report_status;
	public long getSid() {
		return sid;
	}
	public void setSid(long sid) {
		this.sid = sid;
	}
	public long getUid() {
		return uid;
	}
	public void setUid(long uid) {
		this.uid = uid;
	}
	public String getUser_receive_time() {
		return user_receive_time;
	}
	public void setUser_receive_time(String user_receive_time) {
		this.user_receive_time = user_receive_time;
	}
	public String getError_msg() {
		return error_msg;
	}
	public void setError_msg(String error_msg) {
		this.error_msg = error_msg;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getReport_status() {
		return report_status;
	}
	public void setReport_status(String report_status) {
		this.report_status = report_status;
	}
	
	
	
}
